package Lesson3.Lesson3Task1;

public class Calculator {

    double add(double a, double b, double c) {
        return a + b + c;
    }

    double add(int a, double b) {
        return a + b;
    }

    int substract(int a, int c) {
        return a - c;
    }

    double substract(double a, double c) {
        return a - c;
    }

}
